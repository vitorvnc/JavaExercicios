package objetos0809;

public class cliente {
	
 private String nome, email;
 private int idade;
 
   public String getNome() {
	return nome;
   }
   public void setNome(String nomeset) {
	this.nome = nomeset;
   }

   public String getEmail() {
	return email;
   }
   public void setEmail(String emailset) {
	this.email = emailset;
   }
   
   public int getIdade() {
	return idade;
   }
   public void setIdade(int idadeset) {
	this.idade = idadeset;
   }
 
}
