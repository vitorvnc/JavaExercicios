package poli0909;

public abstract class Animal implements AnimalInterface {
	private String nome;
	 private int idade;
	
	
	
 
	 public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

	
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}

  
  
 

}
